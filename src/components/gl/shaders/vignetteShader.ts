import * as THREE from 'three';

export const VignetteShader = {
  uniforms: {
    tDiffuse: { value: null },
    darkness: { value: 1.0 },
    offset: { value: 1.0 },
    vignetteColor: { value: new THREE.Color(0,0,0) }, // Couleur du vignettage
  },
  vertexShader: /* glsl */`
    varying vec2 vUv;
    void main() {
      vUv = uv;
      gl_Position = vec4(position, 1.0);
    }
  `,
  fragmentShader: /* glsl */`
    uniform sampler2D tDiffuse;
    uniform float darkness;
    uniform float offset;
    uniform vec3 vignetteColor;
    varying vec2 vUv;
    
    void main() {
      vec4 texel = texture2D(tDiffuse, vUv);
      
      // Calculate distance from center
      vec2 uv = (vUv - 0.5) * 2.0;
      float dist = dot(uv, uv);
      
      // Create vignette effect
      float vignette = 1.0 - smoothstep(offset, offset + darkness, dist);
      
      // MÃ©lange la couleur du fond et la couleur du vignettage
      vec3 color = mix(vignetteColor, texel.rgb, vignette);
      gl_FragColor = vec4(color, texel.a);
    }
  `
};
